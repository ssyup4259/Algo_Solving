package DP;

import java.util.Scanner;

public class Ex1463 {

	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		int n = sc.nextInt();
		int[] dp = new int[n + 1];

		dp[1] = 0;

		for (int i = 2; i <= n; i++) {
			// i번째는 i-1번째 +1
			dp[i] = dp[i - 1] + 1;
			// 2로나눠지고 1씩 더한것보다 지금이 작다면
			if (i % 2 == 0 && dp[i] > dp[i / 2] + 1) {
				dp[i] = dp[i / 2] + 1;
			}
			// 3으로 나눠지고 1씩더한것보다 지금이 작다면
			if (i % 3 == 0 && dp[i] > dp[i / 3] + 1) {
				dp[i] = dp[i / 3] + 1;
			}
		}
		System.out.println(dp[n]);

	}

}
